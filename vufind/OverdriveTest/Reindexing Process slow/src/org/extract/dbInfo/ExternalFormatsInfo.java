package org.extract.dbInfo;

public class ExternalFormatsInfo {
	private int id;
	private int externalDataId;
	private int formatId;
	private String formatLink;
	private Long dataAdded;
	private Long dateUpdated;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getExternalDataId() {
		return externalDataId;
	}
	public void setExternalDataId(int externalDataId) {
		this.externalDataId = externalDataId;
	}
	public int getFormatId() {
		return formatId;
	}
	public void setFormatId(int formatId) {
		this.formatId = formatId;
	}
	public String getFormatLink() {
		return formatLink;
	}
	public void setFormatLink(String formatLink) {
		this.formatLink = formatLink;
	}
	public Long getDataAdded() {
		return dataAdded;
	}
	public void setDataAdded(Long dataAdded) {
		this.dataAdded = dataAdded;
	}
	public Long getDateUpdated() {
		return dateUpdated;
	}
	public void setDateUpdated(Long dateUpdated) {
		this.dateUpdated = dateUpdated;
	}
	
	
	

}
